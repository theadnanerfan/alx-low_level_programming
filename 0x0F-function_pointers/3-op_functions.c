#include "3-calc.h"
#include <stdlib.h>
#include <stdio.h>
/**
 * op_add - adds
 * @a: first input
 * @b: second input
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subs
 * @a: first input
 * @b: second input
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mults
 * @a: first input
 * @b: second input
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divs
 * @a: first input
 * @b: second input
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: first input
 * @b: second input
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
